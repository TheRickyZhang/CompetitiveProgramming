constexpr int N = 100000;
ll n;

struct cow {
    int l, len, i; // arrive, length, index (priority)
    cow() : l(0), len(0), i(0) {}
    bool operator>(const cow& other) const {
        return i > other.i;
    }
};

int main() {
    setIO("convention2");
    ios::sync_with_stdio(false); cin.tie(nullptr);
    cin>>n;
    vector<cow> cows(n);
    f(i, n) {
        cin>>cows[i].l>>cows[i].len; cows[i].i=i;
    }
    sort(all(cows), [&](const cow& a, const cow& b){ return a.l < b.l; }); // [arrival time, length
    priority_queue<cow, vector<cow>, greater<cow>> q; // Min heap by index
    ll res=0, t=0; ll i=0;
    while(i<n || !q.empty()) {
        if(i<n && cows[i].l <= t) {
            q.push(cows[i]); i++;
        } else if(q.empty()) {
            t=cows[i].l; // Catch up to speed
        } else {
            res = max(res, t-q.top().l);
            // cout<<"cow "<<q.top().i<<" waited from "<<q.top().l<<" "<<t<<en;
            t += q.top().len;
            q.pop();
        }
    }
    cout<<res<<en;
}